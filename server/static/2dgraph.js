async function fetchSuggestions(query) {
    if (query.length < 2) {
        resultsContainer.innerHTML = "";
        resultsContainer.classList.remove("show");
        return;
    }

    try {
        const res = await fetch("/suggest", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ q: query })
        });

        if (!res.ok) throw new Error("Suggestion fetch failed");

        const { results } = await res.json();
        resultsContainer.innerHTML = "";

        if (!results?.length) {
            resultsContainer.innerHTML = '<div class="no-results">No matching courses, programs, or departments</div>';
        } else {
            results.forEach((item, idx) => {
                const div = document.createElement("div");
                div.className = "result-item";
                const title = document.createElement("div");
                title.textContent = `${item.code} — ${item.name || item.title || "(no title)"}`;
                div.appendChild(title);

                const numPrereqs = String(item.num_prereqs ?? "");
                if (numPrereqs !== "") {
                    const prereqMeta = document.createElement("div");
                    prereqMeta.className = "result-prereqs";
                    prereqMeta.textContent = `${numPrereqs} prerequisite${numPrereqs === "1" ? "" : "s"}`;
                    div.appendChild(prereqMeta);
                }

                div.dataset.index = idx;
                div.dataset.code = item.code;

                div.addEventListener("click", () => {
                    queryInput.value = item.code;
                    resultsContainer.classList.remove("show");
                    openExistingNodeOrFetch(item.code);
                });

                resultsContainer.appendChild(div);
            });
        }

        resultsContainer.classList.add("show");
        resultsContainer.scrollTop = 0;
        selectedIndex = -1;
    } catch (err) {
        resultsContainer.innerHTML = '<div class="no-results">Error loading suggestions</div>';
        resultsContainer.classList.add("show");
        resultsContainer.scrollTop = 0;
    }
}

