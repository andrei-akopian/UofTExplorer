function applySoftDepthForce() {
    if (!useShellLayout || !network?.body?.nodes) return;
    const nodes = network.body.nodes;

    for (const id in nodes) {
        const node = nodes[id];
        const targetR = node.options?.targetRadius;

        if (targetR !== undefined && targetR !== null) {
            const dx = node.x;
            const dy = node.y;
            const currentR = Math.sqrt(dx * dx + dy * dy) || 1;

            // 1. Radial Force (Keep them on their ring)
            const dr = targetR - currentR;
            node.x += (dx / currentR) * dr * DEPTH_PULL_STRENGTH;
            node.y += (dy / currentR) * dr * DEPTH_PULL_STRENGTH;

            // 2. Pendulum Bias (Pull X toward 0 to center them at the bottom)
            // This acts like a restoring force for a pendulum
            node.x -= dx * GRAVITY_BIAS;

            // 3. Optional: Slight downward pressure to keep them from floating up the sides
            node.y += GRAVITY_BIAS * 10;
        }
    }
}

