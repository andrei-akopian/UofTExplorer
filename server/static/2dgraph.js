async function fetchGraph(query) {
    if (!query) return;

    currentQueryName = query;  // Store query for statistics display
    showMessage("Loading graph...", "info");
    fetchButton.disabled = true;

    try {
        const payload = buildFetchGraphPayload(query);
        const res = await fetch("/fetch_graph", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify(payload)
        });

        if (!res.ok) {
            const err = await res.json().catch(() => ({}));
            throw new Error(err.error || `Server error (${res.status})`);
        }

        const newData = await res.json();
        if (!newData?.nodes || !newData?.edges) {
            console.log(newData);
            throw new Error("Invalid graph data format");
        }

        renderCurrentQuery(newData.curr_query);
        applyGraphData(newData);
        selectedCourseNode = null;
        openCourseGraphButton.disabled = true;
        showStatisticsButton.disabled = false;  // Enable after graph loads
        closeCoursePanel();
        closeStatisticsPanel();

        // If the query is a course, automatically open the course panel
        if (newData.should_open_course_panel && newData.curr_query?.code) {
            const courseCode = newData.curr_query.code;
            const courseNode = newData.nodes.find(node => node.id === courseCode);
            if (courseNode) {
                showCourseInfo(courseNode);
            }
        }

        showMessage(`Graph loaded (${newData.nodes.length} nodes)`, "success");
        queryInput.value = "";
        if (newData["search"]) {
            setQueryParam("search", newData["search"], true)
        }
    } catch (err) {
        showMessage(`Error: ${err.message}`, "error");
        console.error(err);
    } finally {
        fetchButton.disabled = false;
    }
}

function openExistingNodeOrFetch(query) {
    const trimmedQuery = String(query || "").trim();
    if (!trimmedQuery) return;

    const existingNode = findNodeInCurrentGraph(trimmedQuery);
    if (existingNode) {
        queryInput.value = trimmedQuery;
        focusNode(existingNode);
        showCourseInfo(existingNode);
        showMessage(`Focused ${existingNode.label || existingNode.code || existingNode.id}`, "success");
        return;
    }

    fetchGraph(trimmedQuery);
}

