async function fetchSuggestions(stackKey, query) {
    const stack = stacks[stackKey];
    if (!stack) return;

    if (query.length < 2) {
        stack.results.innerHTML = "";
        hideResults(stack);
        return;
    }

    try {
        const res = await fetch("/suggest_courses", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ q: query })
        });

        if (!res.ok) throw new Error("Suggestion fetch failed");

        const { results } = await res.json();
        stack.results.innerHTML = "";

        if (!results?.length) {
            stack.results.innerHTML = '<div class="no-results">No matching courses</div>';
        } else {
            results.forEach((item, index) => {
                const div = document.createElement("div");
                div.className = "result-item";
                const displayName = item.name || item.title || "(no title)";
                const title = document.createElement("div");
                title.textContent = `${item.code} — ${displayName}`;
                div.appendChild(title);

                const numPrereqs = String(item.num_prereqs ?? "");
                if (numPrereqs !== "") {
                    const prereqMeta = document.createElement("div");
                    prereqMeta.className = "result-prereqs";
                    prereqMeta.textContent = `${numPrereqs} prerequisite${numPrereqs === "1" ? "" : "s"}`;
                    div.appendChild(prereqMeta);
                }

                div.dataset.index = String(index);
                div.dataset.code = item.code;
                div.dataset.name = displayName;

                div.addEventListener("click", () => addCourseToStack(stackKey, item));
                stack.results.appendChild(div);
            });
        }

        stack.results.classList.add("show");
        stack.selectedIndex = -1;
    } catch (error) {
        stack.results.innerHTML = '<div class="no-results">Error loading suggestions</div>';
        stack.results.classList.add("show");
    }
}

function registerStackInteractions(stackKey) {
    const stack = stacks[stackKey];
    if (!stack) return;

    stack.input.addEventListener("input", (event) => {
        clearTimeout(stack.debounceTimer);
        stack.debounceTimer = setTimeout(() => {
            fetchSuggestions(stackKey, event.target.value.trim());
        }, 200);
    });

    stack.input.addEventListener("keydown", (event) => {
        if (!stack.results.classList.contains("show")) return;

        const items = stack.results.querySelectorAll(".result-item");
        if (event.key === "ArrowDown") {
            event.preventDefault();
            if (stack.selectedIndex < items.length - 1) stack.selectedIndex++;
            highlightSelected(stack);
        } else if (event.key === "ArrowUp") {
            event.preventDefault();
            if (stack.selectedIndex > -1) stack.selectedIndex--;
            highlightSelected(stack);
        } else if (event.key === "Enter" && stack.selectedIndex >= 0) {
            event.preventDefault();
            const selected = items[stack.selectedIndex];
            if (!selected) return;
            addCourseToStack(stackKey, {
                code: selected.dataset.code,
                name: selected.dataset.name || ""
            });
        } else if (event.key === "Escape") {
            hideResults(stack);
        }
    });
}

document.addEventListener("click", (event) => {
    Object.values(stacks).forEach((stack) => {
        const clickedInsideInput = stack.input.contains(event.target);
        const clickedInsideResults = stack.results.contains(event.target);
        if (!clickedInsideInput && !clickedInsideResults) {
            hideResults(stack);
        }
    });
});

