function normalizeDepth(depth, minDepth, maxDepth) {
    if (!Number.isInteger(depth)) return null;
    if (minDepth === null || maxDepth === null) return null;

    if (maxDepth === minDepth) {
        if (depth > 0) return 0.8;
        if (depth < 0) return 0.2;
        return 0.5;
    }

    return (depth - minDepth) / (maxDepth - minDepth);
}

function prepareStrategyGraphData(data) {
    const rawNodes = data?.nodes || [];
    const depthValues = rawNodes
        .map((node) => node?.depth)
        .filter((depth) => Number.isInteger(depth));
    const minDepth = depthValues.length ? Math.min(...depthValues) : null;
    const maxDepth = depthValues.length ? Math.max(...depthValues) : null;

    const layerBuckets = new Map();
    rawNodes.forEach((node, index) => {
        const depthKey = Number.isInteger(node?.depth) ? String(node.depth) : "__free__";
        if (!layerBuckets.has(depthKey)) {
            layerBuckets.set(depthKey, []);
        }
        layerBuckets.get(depthKey).push(index);
    });

    const nodes = rawNodes.map((node, index) => {
        const normalizedDepth = normalizeDepth(node?.depth, minDepth, maxDepth);
        const targetY = normalizedDepth === null
            ? null
            : (normalizedDepth - 0.5) * DEPTH_TARGET_SPAN;
        const initialPosition = getInitialNodePosition(node, index, layerBuckets, targetY);

        return {
            ...node,
            id: node.id,
            label: node.label,
            targetY,
            depth: node?.depth ?? null,
            x: initialPosition.x,
            y: initialPosition.y
        };
    });

    const edges = (data?.edges || []).map((edge, index) => ({
        ...edge,
        id: edge.id ?? `${edge.from}-${edge.to}-${index}`
    }));

    return { nodes, edges };
}

function getInitialNodePosition(node, index, layerBuckets, targetY) {
    const depthKey = Number.isInteger(node?.depth) ? String(node.depth) : "__free__";
    const bucket = layerBuckets.get(depthKey) || [index];
    const bucketIndex = bucket.indexOf(index);
    const safeBucketIndex = bucketIndex >= 0 ? bucketIndex : 0;
    const bucketSize = Math.max(bucket.length, 1);
    const jitterX = (Math.random() - 0.5) * 2 * INITIAL_X_JITTER;
    const jitterY = (Math.random() - 0.5) * 2 * INITIAL_Y_JITTER;

    if (targetY === null) {
        const freeFloatX = ((safeBucketIndex + 1) / (bucketSize + 1) - 0.5) * FREE_FLOATING_SPAN;
        const freeFloatY = (Math.floor(safeBucketIndex / 5) - 1.5) * 110;
        return {
            x: freeFloatX + jitterX,
            y: freeFloatY + jitterY
        };
    }

    const normalizedX = bucketSize === 1 ? 0.5 : safeBucketIndex / (bucketSize - 1);
    const x = (normalizedX - 0.5) * LAYER_X_SPAN;
    const yOffset = ((safeBucketIndex % 3) - 1) * 24;
    return {
        x: x + jitterX,
        y: targetY + yOffset + jitterY
    };
}

function applyDepthForce() {
    if (!network?.body?.nodes) return;

    const nodes = network.body.nodes;
    for (const id in nodes) {
        const node = nodes[id];
        const targetY = node.options?.targetY;

        if (targetY === null || targetY === undefined) {
            continue;
        }

        const dy = targetY - node.y;
        node.y += dy * DEPTH_PULL_STRENGTH;
    }
}

function applyStrategyGraphData(data) {
    const prepared = prepareStrategyGraphData(data);

    if (network) {
        network.setData({ nodes: [], edges: [] });
    }
    graphNodes.clear();
    graphEdges.clear();
    graphNodes.add(prepared.nodes);
    graphEdges.add(prepared.edges);
    if (network) {
        network.setData({ nodes: graphNodes, edges: graphEdges });
        network.startSimulation();
    }

    if (network) {
        network.fit({
            animation: {
                duration: 500,
                easingFunction: "easeInOutQuad"
            }
        });
    }
}

