async function sendImmediatePostreqsRequest() {
    const payload = {
        completed_courses: stacks.completed.items
    };

    const immediatePostreqsButton = document.getElementById("immediatePostreqsButton");
    immediatePostreqsButton.disabled = true;

    const requestStatus = document.getElementById("requestStatus");

    try {
        const response = await fetch("/get_immediate_postreqs", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify(payload)
        });

        if (!response.ok) {
            const error = await response.json();
            throw new Error(error.error || "Deconstructed graph request failed");
        }

        const data = await response.json();

        // Apply the deconstructed graph data
        if (data?.nodes && data?.edges) {
            applyStrategyGraphData(data);

            // Display solution courses (desired courses) in text format
            if (data.solution_display) {
                const solutionCourses = Object.values(data.solution_display).join("\n");
                setRequestStatus(solutionCourses, "success");
            } else {
                setRequestStatus(`Deconstructed graph loaded (${data.nodes.length} nodes).`, "success");
            }
        } else {
            throw new Error("Invalid graph data format");
        }
    } catch (error) {
        console.error("Immediate post-reqs request error:", error);
        setRequestStatus(String(error?.message || error), "error");
    } finally {
        immediatePostreqsButton.disabled = false;
    }
}

async function sendStrategyRequest() {
    const payload = buildStrategyPayload();
    const jobId = "job_" + Date.now() + "_" + Math.random().toString(36).substr(2, 9);
    payload.job_id = jobId;

    demoSendButton.disabled = true;

    const progressContainer = document.getElementById("progressContainer");
    const warningContainer = document.getElementById("warningContainer");

    if (!progressContainer) {
        console.error("Progress container not found in DOM!");
        setRequestStatus("UI element missing", "error");
        demoSendButton.disabled = false;
        return;
    }

    progressContainer.style.display = "block";
    warningContainer.style.display = "none";
    setRequestStatus("");

    try {
        // Send initial request, returns immediately with job_id
        const initialResponse = await fetch(PATH_EXPLORER_GRAPH_ENDPOINT, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify(payload)
        });

        if (initialResponse.status !== 202) {
            const error = await initialResponse.json();
            throw new Error(error.error || "Planning request failed");
        }

        // Poll for progress and results
        let isComplete = false;
        startPolling(jobId);
        while (!isComplete) {
            const progressResponse = await fetch(`/pathexplorer_progress/${jobId}`);
            if (!progressResponse.ok) {
                throw new Error("Failed to fetch progress: " + progressResponse.status);
            }

            const data = await progressResponse.json();
            updateProgressDisplay(data);

            // Display warning as soon as it arrives (during solving, not just at completion)
            if (data.warning && warningContainer.style.display === "none") {
                warningContainer.style.display = "block";
                document.getElementById("warningText").textContent = data.warning;
            }

            if (data.status === "complete") {
                isComplete = true;

                // Render graph with data from polling endpoint
                if (!data?.nodes || !data?.edges) {
                    throw new Error("Invalid graph data format");
                }

                applyStrategyGraphData(data);

                // Display solution courses (desired courses) in text format
                if (data.solution_display) {
                    const solutionCourses = Object.values(data.solution_display).join("\n");
                    setRequestStatus(solutionCourses, "success");
                } else {
                    setRequestStatus(`Strategy graph loaded (${data.nodes.length} nodes).`, "success");
                }
                progressContainer.style.display = "none";
            } else if (data.status === "cancelled") {
                isComplete = true;
                setRequestStatus("Solving cancelled.", "info");
                progressContainer.style.display = "none";
            } else if (data.status === "error") {
                throw new Error(data.error || "Solver encountered an error");
            } else {
                // Wait before polling again - keep container visible
                await new Promise(resolve => setTimeout(resolve, 200));
            }
        }
        stopPolling();

        return;
    } catch (error) {
        console.error("Strategy request error:", error);
        setRequestStatus(String(error?.message || error), "error");
        progressContainer.style.display = "none";
    } finally {
        stopPolling();
        demoSendButton.disabled = false;
    }
}

