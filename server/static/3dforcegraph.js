function processGraphData(data) {
  if (!data?.nodes) return { nodes: [], links: [] };

  // 1. Create a Set of valid IDs for quick lookup
  const validNodeIds = new Set(data.nodes.map((n) => String(n.id)));

  const levels = {};
  // Group nodes by depth
  if (useShellLayout) {
    data.nodes.forEach((n) => {
      const d =
        n.depth !== null && n.depth !== undefined ? parseInt(n.depth) : null;
      if (d !== null) {
        if (!levels[d]) levels[d] = [];
        levels[d].push(n);
      }
    });
  }

  // 2. Calculate dynamic radii (same as before)
  const levelRadii = {};
  let currentRadius = 0;
  const sortedDepths = Object.keys(levels).map(Number).sort((a, b) => a - b);

  sortedDepths.forEach((depth) => {
    const nodeCount = levels[depth].length;
    const requiredRadius = (nodeCount * NODE_GIRTH) / (2 * Math.PI);
    currentRadius += Math.max(MIN_RADIUS_STEP, requiredRadius);
    levelRadii[depth] = currentRadius;
  });

  // 3. Map nodes
  const nodes = data.nodes.map((n) => {
    const depth =
      n.depth !== null && n.depth !== undefined ? parseInt(n.depth) : null;
    const targetRadius = depth !== null ? levelRadii[depth] : null;

    return {
      ...n,
      id: String(n.id), // Ensure ID is a string for consistent matching
      depth,
      targetRadius: useShellLayout ? targetRadius : null,
      x: (Math.random() - 0.5) * 120,
      z: (Math.random() - 0.5) * 120,
      y: useShellLayout && depth !== null ? -levelRadii[depth] : (Math.random() - 0.5) * 120,
    };
  });

  // 4. Filter links that refer to missing or "None" nodes
  const links = (data.edges || [])
    .filter((e) => {
      const hasSource =
        e.from !== null && e.from !== undefined && e.from !== "None";
      const hasTarget = e.to !== null && e.to !== undefined && e.to !== "None";
      const exists =
        validNodeIds.has(String(e.from)) && validNodeIds.has(String(e.to));
      if (!(hasSource && hasTarget && exists)) {
        console.log("Filtered invalid edge:", e);
      }
      return hasSource && hasTarget && exists;
    })
    .map((e) => ({
      source: String(e.from),
      target: String(e.to),
    }));

  return { nodes, links };
}

