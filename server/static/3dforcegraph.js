let particleIntervalId = null;
let activeParticleCount = 0;

function releaseParticleSlot() {
  activeParticleCount = Math.max(0, activeParticleCount - 1);
}

function emitParticleCapped(link) {
  if (!link || activeParticleCount >= MAX_ACTIVE_PARTICLES) return false;

  try {
    Graph.emitParticle(link);
    activeParticleCount += 1;
    setTimeout(releaseParticleSlot, PARTICLE_LIFETIME_MS);
    return true;
  } catch (error) {
    return false;
  }
}

function startEmitInterval() {
  if (particleIntervalId) return;
  particleIntervalId = setInterval(() => {
    if (document.visibilityState === "hidden") return;
    const links = graphData?.links || [];
    if (!links.length) return;
    if (links.length > MAX_EDGES_FOR_PARTICLES) return; // skip for performance

    // Deterministic emission: emit on ALL edges once per interval (in order).
    // Prefer emitting the graph's internal link objects when available.
    const gd = Graph.graphData();
    const linkSource = (gd && gd.links) ? gd.links : links;

    for (let i = 0; i < linkSource.length; i++) {
      if (activeParticleCount >= MAX_ACTIVE_PARTICLES) break;
      const link = linkSource[i];
      if (!emitParticleCapped(link)) {
        // If the link object isn't the same instance, try matching by ids
        try {
          const internal = gd && gd.links && gd.links.find(l => String(l.source) === String(link.source) && String(l.target) === String(link.target));
          emitParticleCapped(internal);
        } catch (err) {
          // swallow - emission is non-critical
        }
      }
    }
  }, PARTICLE_EMIT_INTERVAL_MS);
}

function stopEmitInterval() {
  if (!particleIntervalId) return;
  clearInterval(particleIntervalId);
  particleIntervalId = null;
}

