function escapeHtml(value) {
  return String(value).replace(/[&<>"']/g, (char) => {
    const entities = {
      "&": "&amp;",
      "<": "&lt;",
      ">": "&gt;",
      '"': "&quot;",
      "'": "&#39;",
    };
    return entities[char];
  });
}

// renderJsonValue now returns DOM elements instead of HTML strings.
// - Primitives -> <span>
// - Objects/arrays -> <pre> with pretty JSON
// - null/undefined -> <span> with "None"
function renderJsonValue(value) {
  if (value === null || value === undefined) {
    const span = document.createElement("span");
    span.textContent = "None";
    return span;
  }

  if (typeof value === "string" || typeof value === "number" || typeof value === "boolean") {
    const span = document.createElement("span");
    span.textContent = String(value);
    return span;
  }

  // Complex object/array: pretty-print inside <pre>
  const pre = document.createElement("pre");
  try {
    pre.textContent = JSON.stringify(value, null, 2);
  } catch (err) {
    // Fallback if value isn't serializable
    pre.textContent = String(value);
  }
  return pre;
}

